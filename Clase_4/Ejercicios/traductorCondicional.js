// Traductor condicional - switch

let traductor = " ";
  switch (traductor){
    case "Perro":
        console.log("La traducción de 'Perro' es 'Dog'");
        break;

    case "Gato":
        console.log("La traducción de 'Gato' es 'Cat'");
        break;

    case "Puerta":
        console.log("La traducción de 'Puerta' es 'Door'");
        break;

    case "Ventana":
        console.log("La traducción de 'Ventana' es 'Window'");
        break;
    case "Mesa":
        console.log("La traducción de 'Mesa' es 'Table'");

    default:
    console.log("La palabra ingresada es incorrecta");
    break;
  }