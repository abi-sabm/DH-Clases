// Dato del tipo number
let edad = 28;
console.log(edad);

// Dato del tipo number con decimal
let precio = 150.65;
console.log(precio);

// Dato del tipo string con comillas dobles
let nombre = "Abichuela";
console.log(nombre);

// Dato del tipo string con comillas simples
let apellido = 'Barroso';
console.log(apellido);

// Dato del tipo booleano true
let verdadero = true;
console.log(verdadero);

// Dato del tipo booleano false
let falso = false;
console.log(falso);

// Dato del tipo Not a Number
let notANumber = NaN;
console.log(notANumber);

// Dato del tipo Null
let nulo = null;
console.log(nulo);

// Dato del tipo Undefined
let sinValor = "Sin valor";
console.log(sinValor);

// Dato del tipo Objeto Literal
let persona = {
    nombre : "Lucas",
    apellido : "Alvarez",
    edad : 28
}
console.log (persona);

// Dato del tipo Array
let paises = [
    "Argentina", "Venezuela", "Colombia"
]
console.log(paises);